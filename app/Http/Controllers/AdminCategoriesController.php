    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => 'required',
            'image'    => 'required|image|mimes:jpg,jpeg,gif,png',
        ]);

        $category = new Category();
        $category->name = $request->input('name');

        //upload image
        if($request->hasFile('image')) {
            $imageExt = $request->File('image')->getClientOriginalExtension();
            $imageName = rand(0, 10) . time()  . '.' . $imageExt;
            $location = public_path('storage/categories/');
            $request->file('image')->move($location, $imageName);

            $category->image = $imageName;
        }

        $category->save();
        return redirect(route('categories.index'))->with('msg', 'تم حفظ القسم بنجاح');

    }

